var garageFinder = {
  userLat: null,
  userLon: null,
  filtered: [],
  shown: 0,
  perPage: 20,

  init: function() {
    this.filtered = UK_MOTORCYCLE_GARAGES.slice();
    this.shown = 0;
    this.renderList();
  },

  haversine: function(lat1, lon1, lat2, lon2) {
    var R = 3959;
    var dLat = (lat2 - lat1) * Math.PI / 180;
    var dLon = (lon2 - lon1) * Math.PI / 180;
    var a = Math.sin(dLat/2) * Math.sin(dLat/2) +
            Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
            Math.sin(dLon/2) * Math.sin(dLon/2);
    return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  },

  locateMe: function() {
    if (!navigator.geolocation) {
      alert('Geolocation is not supported by your browser.');
      return;
    }

    document.getElementById('garageLocating').style.display = 'block';
    var self = this;

    navigator.geolocation.getCurrentPosition(
      function(pos) {
        self.userLat = pos.coords.latitude;
        self.userLon = pos.coords.longitude;
        document.getElementById('garageLocating').style.display = 'none';

        self.filtered = UK_MOTORCYCLE_GARAGES.map(function(g) {
          var dist = self.haversine(self.userLat, self.userLon, g.lat, g.lon);
          return Object.assign({}, g, { dist: dist });
        }).sort(function(a, b) { return a.dist - b.dist; });

        self.shown = 0;
        document.getElementById('garageSortLabel').textContent = 'Sorted by distance from you';
        self.renderList();
      },
      function(err) {
        document.getElementById('garageLocating').style.display = 'none';
        if (err.code === 1) {
          alert('Location access denied. You can search by name or postcode instead.');
        } else {
          alert('Could not get your location. Try searching instead.');
        }
      },
      { enableHighAccuracy: false, timeout: 10000 }
    );
  },

  search: function(query) {
    var q = query.toLowerCase().trim();
    if (!q) {
      this.filtered = this.userLat ? 
        UK_MOTORCYCLE_GARAGES.map(function(g) {
          return Object.assign({}, g, { dist: garageFinder.haversine(garageFinder.userLat, garageFinder.userLon, g.lat, g.lon) });
        }).sort(function(a,b) { return a.dist - b.dist; }) :
        UK_MOTORCYCLE_GARAGES.slice();
    } else {
      this.filtered = UK_MOTORCYCLE_GARAGES.filter(function(g) {
        return (g.n && g.n.toLowerCase().includes(q)) ||
               (g.a && g.a.toLowerCase().includes(q)) ||
               (g.w && g.w.toLowerCase().includes(q));
      });
      if (this.userLat) {
        var self = this;
        this.filtered = this.filtered.map(function(g) {
          return Object.assign({}, g, { dist: self.haversine(self.userLat, self.userLon, g.lat, g.lon) });
        }).sort(function(a,b) { return a.dist - b.dist; });
      }
    }
    this.shown = 0;
    document.getElementById('garageCount').textContent = this.filtered.length + ' garage' + (this.filtered.length !== 1 ? 's' : '') + ' found';
    this.renderList();
  },

  renderList: function() {
    var end = Math.min(this.shown + this.perPage, this.filtered.length);
    var html = '';

    for (var i = this.shown; i < end; i++) {
      var g = this.filtered[i];
      var distText = g.dist != null ? '<span class="gf-distance"><i class="fas fa-location-dot"></i> ' + g.dist.toFixed(1) + ' miles</span>' : '';
      var typeLabel = g.t === 'r' ? '<span class="gf-type-badge gf-type-repair">Repair</span>' : '<span class="gf-type-badge gf-type-dealer">Dealer / Shop</span>';

      var actions = '';
      if (g.p) actions += '<a href="tel:' + g.p.replace(/\s/g, '') + '" class="gf-action"><i class="fas fa-phone"></i> ' + g.p + '</a>';
      if (g.w) {
        var url = g.w.startsWith('http') ? g.w : 'https://' + g.w;
        actions += '<a href="' + url + '" target="_blank" rel="noopener" class="gf-action"><i class="fas fa-globe"></i> Website</a>';
      }
      actions += '<a href="https://www.google.com/maps/dir/?api=1&destination=' + g.lat + ',' + g.lon + '" target="_blank" rel="noopener" class="gf-action"><i class="fas fa-diamond-turn-right"></i> Directions</a>';

      html += '<div class="gf-card">' +
        '<div class="gf-card-header">' +
          '<div>' +
            '<h3 class="gf-card-name">' + g.n + '</h3>' +
            (g.a ? '<p class="gf-card-address"><i class="fas fa-map-pin"></i> ' + g.a + '</p>' : '') +
          '</div>' +
          '<div class="gf-card-meta">' +
            typeLabel +
            distText +
          '</div>' +
        '</div>' +
        (g.h ? '<p class="gf-card-hours"><i class="fas fa-clock"></i> ' + g.h + '</p>' : '') +
        '<div class="gf-card-actions">' + actions + '</div>' +
      '</div>';
    }

    if (this.shown === 0) {
      document.getElementById('garageList').innerHTML = html;
    } else {
      document.getElementById('garageList').innerHTML += html;
    }
    this.shown = end;

    var loadMore = document.getElementById('garageLoadMore');
    if (loadMore) loadMore.style.display = end < this.filtered.length ? 'block' : 'none';
  },

  loadMore: function() {
    this.renderList();
  }
};
